import UIKit

extension UIFont {
    static func avenir16() -> UIFont? {
        return UIFont.init(name: "avenir", size: 16)
    }
    
    static func avenir20() -> UIFont? {
        return UIFont.init(name: "avenir", size: 20)
    }
    
    static func avenir26() -> UIFont? {
        return UIFont.init(name: "avenir", size: 26)
    }
    
    static func avenir32() -> UIFont? {
        return UIFont.init(name: "avenir", size: 32)
    }
}
